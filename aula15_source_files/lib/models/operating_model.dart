class OperatingModel {
  String _first;
  String _second;

  OperatingModel({String first, String second}) {
    this._first = first;
    this._second = _second;
  }

  String get First => _first;
  set First(String first) => _first = first;

  String get Second => _second;
  set Second(String second) => _second = second;
}
